package Ejercicio2;

public class Pair {
	private int key;
	private int value;
	public Pair(int key, int value) {
		super();
		this.key = key;
		this.value = value;
	}
	public int getKey() {
		return key;
	}
	public int getValue() {
		return value;
	}
	
	
	
	
	
}
